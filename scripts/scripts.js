



function DarkMode() {
    document.getElementById('PageBody').style="background-color: #363635;";


}

function LightMode() {
    document.getElementById('PageBody').style="background-color: #FFFFFF;";
    document.getElementsByClassName('dark').style="color #FFFFFF;";
    document.getElementsByClassName('light').style.color="#FFFFFF;";


}
function Default() {
    document.getElementById('PageBody').style="background-color: #;";


}